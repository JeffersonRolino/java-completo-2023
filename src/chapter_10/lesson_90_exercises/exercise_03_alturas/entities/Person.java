package chapter_10.lesson_90_exercises.exercise_03_alturas.entities;

public class Person {
    String name;
    int age;
    double height;

    public Person() {
    }

    public Person(String name, int age, double height) {
        this.name = name;
        this.age = age;
        this.height = height;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public double getHeight() {
        return height;
    }
}
