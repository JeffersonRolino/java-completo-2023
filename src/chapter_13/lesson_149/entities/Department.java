package chapter_13.lesson_149.entities;

public class Department {
    private String name;


    //***********************************************************************************************
    // CONSTRUCTORS
    //***********************************************************************************************

    public Department() {
    }

    public Department(String name) {
        this.name = name;
    }


    //***********************************************************************************************
    // GETTERS AND SETTERS
    //***********************************************************************************************

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    //***********************************************************************************************
    // METHODS
    //***********************************************************************************************

    @Override
    public String toString() {
        return this.name;
    }
}
